int main()
{   
    ll N;
    cin >> N;
    vector<int> digit;
    for (int i = 0; i < 63; i++){
        if (N & 1LL << i) digit.push_back(i);
    }
    int size = digit.size();
    for (int num = 0; num < (1 << size); num++){
        ll ans = 0;
        for (int i = 0; i < size; i++){
            if (num & (1 << i)){
                ans += 1LL << digit[i]; 
            }
        }
        cout << ans << endl;
    }
}